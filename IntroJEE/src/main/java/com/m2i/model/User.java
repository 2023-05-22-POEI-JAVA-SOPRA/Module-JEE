package com.m2i.model;

public class User {
	public String name;
	public String email;
	public String password;
	public int age;
	
	public User(String name, String email, String password, int age) {
		super();
		this.name = name;
		this.email = email;
		this.password = password;
		this.age = age;
	}
}



